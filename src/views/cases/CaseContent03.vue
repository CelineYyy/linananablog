          <div class="maintitle text-center">Facebook 重大改版背后，揭晓了2019年的4个关键设计趋势</div>
          <div
            class="note-date text-center"
          >2019-05-24&nbsp;&nbsp;&nbsp;&nbsp;in&nbsp;&nbsp;作品集/UI设计</div>
          <div class="line"></div>
          <div class="note-content">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作为一个世界级的社交媒体平台，Facebook 如今的用户体量已经相当可观了。
            不过由于种种原因，国内少有人会主动关注相关的信息。作为每年 Facebook
            团队的盛事，F8 大会上通常会发布新产品，公布新功能，呈现新趋势。
            虽然 F8 大会所涉及到的产品我们大多不太可能用得上，但是关于设计、体验和产品，
            它所呈现出来的趋势，无疑是价值非凡的。
            <br />
            <br />
            <h3>关键词：隐私</h3>
            <br />对于绝大多数的国内用户而言，Facebook 是一个相对遥远的社交平台。
            但是这并不影响 Facebook 成为世界上最有影响力的社交媒体平台之一。
            在过去的十年之中，Facebook 正如同扎克伯格所期望的那样，逐渐构建成为一个庞大的全球社区。
            与此同时，庞大的体量和巨大的用户基数以及相对放任的用户隐私政策，使得 Facebook
            所投下的阴影同样巨大，这导致它在英国脱欧和美国大选中扮演了不光彩的角色，
            而这应该只是诸多不良后果之一。作为一个社交平台，Facebook 本身还有很多问题，
            但是这次 F8 大会上，整个 Facebook 主动站出来面对这一切。
            <br />未来应该是私密的。
            <br />
            <br />「噩梦般的日子」应该是正在洗心革面的扎克伯格和 Facebook 在过去几年间的真实写照。
            败在隐私政策上的 Facebook，在这一次的 F8 大会上毅然决然地选择了「隐私」作为主题。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic01.png" />
            「也许在隐私权这件事情上，我们所拥有的口碑并不是最好的。
            但是我承诺我们会尽力将这件事情做到最好，为我们的产品开启新的篇章。」
            <br />
            <br />扎克伯格在 F8 大会的舞台上，以这句话结束了他的开场白。
            洗心革面的 Facebook 相应地也带来了全新的设计和几经优化过的若干新产品、新功能。
            <br />
            <br />
            <h3>新设计：FB5</h3>
            <br />Facebook 的视觉设计经历过四次大幅度的重设计，作为一个承载大量成熟产品的平台，Facebook 的每一次变化通常都会影响到大量的公司和许多平台产品，
            从某种意义上，Facebook 重设计所带来的影响并不亚于 iOS 和 Android 的设计更新。
            <img
              class="workpic"
              src="./../../assets/img/workpic02.png"
            />
            <br />
            <br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作为第五个大版本，Facebook 将这一版本的重设计命名为 FB5。这套全新的设计彻底去掉了之前经典的 Facebook 蓝为主的整套配色方案，整套设计走的是简约的白色风格为主，并且紧跟时下各大平台的 UI 趋势，推出了相应的深色系的主题版本，不过黑色主题并没有在第一时刻更新，甚至白色主题的更新也是在全世界范围内逐步推广更新，
            就目前的反馈信息来看，各国用户所看到的新的 FB5 风格下的 UI 界面细节，还有着微妙的差别。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic03.png" />
            <br />
            <br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前，按照 Facebook 官方的说法，深色模式界面将会优先在视频平台 Facebook Watch 上登录，
            随后会逐步推广到诸如 Facebook Messenger 等 APP 上。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic04.png" />
            <br />
            <br />目前，Facebook 的官方设计中心中还没有相关的设计素材和资源更新，
            估计还需要等 Facebook 官方的更新跟上之后，才会逐步面向大众推出。
            <br />
            <br />
            <h3>4个重要的设计趋势</h3>
            <br />用户隐私是这次 F8 大会的核心议题，对于用户个人信息
            和诉求的关注也是这一次 Facebook 平台和应用更新的重要核心。
            <br />
            <br />作为很少会用到 Facebook 的国内用户，逻辑上而言这些平台和应用的更新，同我们关系不大。但是这次 F8 大会最有价值的内容，是这些更新所呈现出来的设计趋势。
            作为一个世界级的平台，它的关注点无疑有着重要的参考价值。
            <br />
            <br />
            <h3>更简约：降低无关信息干扰</h3>
            <br />和 5 年前相比，如今用户在任何一个平台上所接收到的信息，提高了近乎一个数量级。其中主动推送到用户眼前的通知和各种信息，频率已经非常密集了。
            无论是桌面端还是移动端，高度饱和的视觉信息，让原本更重要的内容很难被凸显出来。
            <img
              class="workpic"
              src="./../../assets/img/workpic05.png"
            />
            <br />作为一套全新的设计规范，FB5 的整套设计语言无疑是统一一致的。更重要的是，新的 FB5 的设计语言通过削减枝节，
            让页面中的内容和信息更突出，在这样的设计之下，用户无疑更容易注意到内容本身。
            <br />由繁入简，这是大势所趋。
            <br />
            <br />
            <h3>更轻量：对抗越来越重的时代</h3>
            <br />用户等待网页加载的平均时长，在 2012 年的时候是 9 秒，而到 2016 年的时候只有 2 秒。
            <br />
            <br />英国卫报通过自己 Ophan 平台统计用户阅读文章的时长时候发现，绝大多数的用户阅读长文章的时候，只会花费 10 秒左右快速扫读。
            <br />
            <br />而用户在浏览微博、Twitter 这样的信息流式的社交媒体内容的时候，每条内容的平均停留时长不会超过 1.5 秒。
            <br />
            <br />设计要轻量化，内容要轻量化，Facebook 甚至在产品安装包上竭尽全力做轻量化，轻量化意味着更快速。
            更快下载，更快打开，更快获得。而这也是整个发布会当中最值得注意的细节。
            <img
              class="workpic"
              src="./../../assets/img/workpic06.png"
            />
            Facebook Massenger 作为 Facebook 社交生态中的核心，这次更新的目标不是高大全，而是更轻量──安装包从最初的 127M 缩减到 30M 以内，更快安装。而 FB5 的整个设计方向，也暗合了这次轻量化的诉求。
            <br />
            <br />与此同时，Facebook 官方公布的 APP 打开速度是在 1.3 秒以内，用户立刻就可以看到收到的信息。有空的同学可以自己测试一下打开微信和微博需要多长时间，作为对比。
            <br />
            <br />作为一款更加轻量级的 APP，Facebook Massenger 也变得更加省电。注意到没有，从现在开始，省电已经成为了一个 APP 的卖点了。
            <br />
            <br />在 5G 和高性能芯片成为主流的今天，Facebook 并没有在核心应用上继续做加法，而是向着相反方向，「毫秒必争」地追求疾速体验。
            <br />
            <br />
            <h3>更沉浸：聚焦更重要的内容</h3>
            <br />
            <img class="workpic" src="./../../assets/img/workpic07.png" />
            <br />
            <br />从某种意义上来说，深色系的界面主题并不是什么稀奇的设计，只不过，对于影响广泛的世界级平台产品而言，在这件事情上会更加谨慎，毕竟深色系的界面并不是简单的背景由白变黑。
            比如苹果的 Mac 系统也只是在 macOS 13 Mojave 上才开始应用 Dard mode ，而 iOS 系统至今还没有推出相应的深色模式。（小道消息称今年 6 月的 WWDC 上我们将在新的 iOS 系统中看到深色模式）
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic08.png" />
            <br />
            <br />Facebook 旗下的系列产品，根据重要性和所需专注度的程度，会先后更新深色模式，其中最先更新的是视频娱乐内容平台 Facebook Watch，而社交通讯核心
            Facebook Massenger 和网页版会紧随其后，并且分梯次地为世界各国的用户推送，逐渐铺开。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic09.png" />
            <br />
            <br />深色系主题的推出，势在必行。现代人每天盯着屏幕的时间越来越长，深色系主题不仅仅能够在一定程度上缓解视力疲劳（尤其在晚上），
            更重要的是，深色模式是一种典型的「沉浸模式」。
            <br />
            <br />“这其实不仅仅和对比度有关系，它和用户的日常使用环境和时间也有着极深的关联：绝大多数的娱乐活动都发生在晚上，
            并且大家会愉悦地享受相对昏暗的房间环境。丰富多彩的视觉内容，也同样会在深色系的 UI 上显得更加炫目……昏暗的环境让人放松和沉浸，
            太亮的背景则会让人紧张，这也是深色系 UI 的设计来源和基础：设备、内容、活动和场景。”──《想使用深色系UI，这些注意事项你一定要了解》
            <br />
            <br />信息量的暴增和用户注意力的涣散直接相关，而沉浸式的设计，则是数字产品对抗这种劣化趋势的有效手段，
            也是用户体验提升的一个关键点。
            <br />
            <br />当然，在产品的沉浸化设计上，Facebook 所做的工作并不只在深色主题上。色彩更加鲜亮的可定制对话框、
            几经优化后的交互路径、不用数据线约束的 VR 设备 OCULUS 则在沉浸式的体验上走的更远。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic10.png" />
            △ Photo by Amelia Holowaty Krales / The Verge
            <br />
            <br />
            <h3>更个人：专注个人的内在诉求</h3>
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic11.png" />
            <br />
            <br />在隐私政策上连续栽跟头的 Facebook ，将隐私和安全作为这次发布会上的核心议题来推进。
            这一点几乎可以从发布会上平台更新乃至每个产品上都有所体现。以往各大科技企业在发布会
            上都会强调用户体验，强调用户，但是从来没有一场发布会能像 2019 年这场 F8 大会上这样，
            所有上台演讲的 Facebook 负责人话里话外都说着「你最重要」这四个字。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic12.png" />
            <br />
            <br />不止是在功能上围绕着用户的隐私和安全来推进，在具体的产品上，也大多围绕着用户本身的个人诉求、垂直到更内化的细节上去做规划。
            <br />
            <br />以 Groups Tab 为例，它会在现有基础上，帮助用户更快匹配上符合自己兴趣爱好的圈子；
            Meet New Friends 功能，则专注帮助用户开拓新的圈子；Facebook Dating
            则是聚焦于恋爱和亲密关系的运营。值得注意的是，Facebook 非常贴心地将
            Facebook Dating 中用户所使用的帐号将会和自己的主帐号保持独立的关系，
            恋爱相关的信息可以只对部分用户可见，私密性和可控性更强。而涉及到恋爱匹配的 Secret Crush
            也是类似，只有当双方在各方面都「呼应上了」之后，才会被告知匹配成功。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic13.png" />
            <br />
            <br />如今已经属于 Facebook 旗下的社交应用 Instagram
            也在这次发布会上推出了一系列的功能更新，最直接的变化，是更加强大的相机功能。
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic14.png" />
            <br />
            <br />但是，更加令人值得关注的，是 Instagram 降低了点赞功能的优先级和权重，并且更加关注用户的敏感情绪──他们推出了 Away mode。
            <br />
            <br />当用户处于脆弱敏感时期的时候，打开 Away mode 之后，将在保留帐号的情况下，不会收到各种活动通知和提醒。
            <br />
            <br />「在生命中的敏感时期里，比如处于情绪低潮或是正在分手的阶段，和灵敏的社交媒体切断关系。」
            <br />
            <br />
            <img class="workpic" src="./../../assets/img/workpic15.png" />
            <br />
            <br />此外，Instagram 还准备干预网络暴力。一旦系统检测到有负面评论的时候，就会主动「干预」，
            给予发布负面评论的用户予以警告提示。Instagram 官方并非直接阻止用户发布内容，
            而是希望借由一系列的处理，让用户停下来想一下。（重新组织一下语言？）
            <br />
